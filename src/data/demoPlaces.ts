import type { MenuItem, OpeningHours, Place, TimeRange } from '../types';

const r = (open: string, close: string): TimeRange => ({ open, close });

const week = (
  weekdays: TimeRange[],
  saturday: TimeRange[] = weekdays,
  sunday: TimeRange[] = weekdays
): OpeningHours => ({
  monday: weekdays,
  tuesday: weekdays,
  wednesday: weekdays,
  thursday: weekdays,
  friday: weekdays,
  saturday,
  sunday,
});

const item = (
  id: string,
  name: string,
  description: string,
  priceLevel: 1 | 2 | 3,
  preparationMinutes: number,
  tags: MenuItem['tags'],
  flags: Partial<
    Pick<
      MenuItem,
      | 'isVegetarian'
      | 'isVegan'
      | 'isWarm'
      | 'isSweet'
      | 'isLight'
      | 'isQuick'
      | 'isHealthy'
    >
  >
): MenuItem => ({
  id,
  name,
  description,
  priceLevel,
  preparationMinutes,
  tags,
  isVegetarian: flags.isVegetarian ?? false,
  isVegan: flags.isVegan,
  isWarm: flags.isWarm ?? false,
  isSweet: flags.isSweet ?? false,
  isLight: flags.isLight ?? false,
  isQuick: flags.isQuick ?? preparationMinutes <= 10,
  isHealthy: flags.isHealthy ?? false,
});

